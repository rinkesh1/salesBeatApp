public class ActivityTrackingItem {

    private String date, walkDistance, runningDistance, bicycleDistance, vehicleDistance;

    public String getDate() {
        return this.date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getWalkDistance() {
        return this.walkDistance;
    }

    public void setWalkDistance(String walkDistance) {
        this.walkDistance = walkDistance;
    }

    public String getRunningDistance() {
        return this.runningDistance;
    }

    public void setRunningDistance(String runningDistance) {
        this.runningDistance = runningDistance;
    }

    public String getBicycleDistance() {
        return this.bicycleDistance;
    }

    public void setBicycleDistance(String bicycleDistance) {
        this.bicycleDistance = bicycleDistance;
    }

    public String getVehicleDistance() {
        return this.vehicleDistance;
    }

    public void setVehicleDistance(String vehicleDistance) {
        this.vehicleDistance = vehicleDistance;
    }

}
